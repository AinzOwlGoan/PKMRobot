function [ G, F, f, J ] = pol357( par , x )

if( x >= 0 && x <= 1 )
    J = 6*35/8-60*21/4*(x^2)+210*15/8*(x^4);
    f = 6*35/8*(x)-20*21/4*(x^3)+42*15/8*(x^5);
    F = 3*35/8*(x^2)-5*21/4*(x^4)+7*15/8*(x^6);
    G = 35/8*(x^3)-21/4*(x^5)+15/8*(x^7);
else
    J=0;
    f=0;
    F=0;
    G=0;
end
